def get_welcome_message(lang: str = None) -> str:
    """
    Returns the welcome message in all 3 languages if no lang specified,
    or in a specific language if provided.
    Used on page load before language is detected.
    """
    if lang:
        return TEMPLATES.get(lang, TEMPLATES["en"])["welcome"]

    # Show all 3 on first load
    return (
        TEMPLATES["en"]["welcome"] + "\n\n"
        + "— — —\n\n"
        + TEMPLATES["fr"]["welcome"] + "\n\n"
        + "— — —\n\n"
        + TEMPLATES["ar"]["welcome"]
    )


def get_template(key: str, lang: str = "en", **kwargs) -> str:
    """
    Retrieves any template string by key and language.
    Supports optional string formatting via kwargs.

    Example:
        get_template("greet_name", lang="fr", name="Yassine")
        → "Ravie de te rencontrer, Yassine 🌿 Comment tu te sens aujourd'hui ?"
    """
    lang_templates = TEMPLATES.get(lang, TEMPLATES["en"])
    text = lang_templates.get(key, TEMPLATES["en"].get(key, ""))
    if kwargs:
        try:
            text = text.format(**kwargs)
        except KeyError:
            pass
    return text


def get_followup(lang: str = "en") -> str:
    """Returns a random solution follow-up question in the right language."""
    import random
    followups = TEMPLATES.get(lang, TEMPLATES["en"])["followup_solutions"]
    return random.choice(followups)


def get_session_end(lang: str = "en", name: str = None) -> str:
    """Returns the session closing message, personalized if name is known."""
    if name:
        return get_template("session_end", lang=lang, name=name)
    return get_template("session_end_no_name", lang=lang)